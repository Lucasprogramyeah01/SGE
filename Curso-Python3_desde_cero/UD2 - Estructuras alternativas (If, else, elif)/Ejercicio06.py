# Programa que lea una cadena por teclado y compruebe si dicha cadena está escrita en mayúsculas.

cadena = input("Introduzca una cadena de caracteres: ")

if cadena == cadena.upper():
    print("Su cadena está escrita entera en mayúsculas.")
else:
    print("Su cadena NO está escrita entera en mayúsculas.")
